package com.example.projectexample.triangle;

public class PointItem {

    float x;
    float y;

    public PointItem() {

    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
